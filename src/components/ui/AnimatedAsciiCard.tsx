interface AnimatedAsciiCardProps {
  type: 'ALGORITHMS' | 'AUTOMATA' | 'VISION';
}

export function AnimatedAsciiCard({ type }: AnimatedAsciiCardProps) {
  const [frameIndex, setFrameIndex] = useState(0);

  let frames: string[] = [];
  if (type === 'ALGORITHMS') frames = ALGO_FRAMES;
  else if (type === 'AUTOMATA') frames = AUTOMATA_FRAMES;
  else if (type === 'VISION') frames = VISION_FRAMES;

  useEffect(() => {
    const interval = setInterval(() => {
      setFrameIndex((prev) => (prev + 1) % frames.length);
    }, 1000); // 1 second per frame
    return () => clearInterval(interval);
  }, [frames.length]);

  return (
    <pre className="font-mono text-[9px] leading-[13px] text-white select-none whitespace-pre text-left w-full h-[150px] flex items-center justify-center">
      {frames[frameIndex]}
    </pre>
  );
}
